package main

import (
	"flag"
	"fmt"
	"go/format"
	"go/types"
	"log"
	"os"
	"sort"
	"strings"

	"golang.org/x/tools/go/packages"

	. "github.com/dave/jennifer/jen"
)

var scannedPkgs = []string{
	"k8s.io/api/apps/v1",
	"k8s.io/api/autoscaling/v2",
	"k8s.io/api/batch/v1",
	"k8s.io/api/core/v1",
	"k8s.io/api/networking/v1",
	"k8s.io/api/policy/v1",
	"k8s.io/api/rbac/v1",
	"k8s.io/api/storage/v1",
	"k8s.io/apimachinery/pkg/apis/meta/v1",
	"k8s.io/apimachinery/pkg/api/resource",
	"k8s.io/apimachinery/pkg/util/intstr",
}

func main() {
	outFlag := flag.String("out", "", "output file path")
	pkgFlag := flag.String("package", "support", "package name for the generated file")
	flag.Parse()

	if *outFlag == "" {
		log.Fatal("-out flag is required")
	}

	// Load all scanned packages
	cfg := &packages.Config{
		Mode: packages.NeedTypes | packages.NeedName | packages.NeedSyntax | packages.NeedTypesInfo,
	}
	pkgs, err := packages.Load(cfg, scannedPkgs...)
	if err != nil {
		log.Fatalf("loading packages: %v", err)
	}

	// Build a set of all struct types from scanned packages, keyed by *types.Named
	type structInfo struct {
		named     *types.Named
		struc     *types.Struct
		pkg       *packages.Package
		assertNam string // assertion struct name
	}

	knownStructs := map[string]*structInfo{} // key: pkgPath + "." + name
	var orderedKeys []string

	// First pass: collect all structs
	nameCount := map[string]int{} // count how many packages define a struct with the same name
	for _, pkg := range pkgs {
		if pkg.Types == nil {
			continue
		}
		scope := pkg.Types.Scope()
		for _, name := range scope.Names() {
			obj := scope.Lookup(name)
			named, ok := obj.Type().(*types.Named)
			if !ok {
				continue
			}
			_, ok = named.Underlying().(*types.Struct)
			if !ok {
				continue
			}
			if !obj.Exported() {
				continue
			}
			if strings.HasSuffix(name, "List") || strings.HasSuffix(name, "Status") {
				continue
			}
			nameCount[name]++
		}
	}

	// Second pass: build structInfo with disambiguated names
	for _, pkg := range pkgs {
		if pkg.Types == nil {
			continue
		}
		scope := pkg.Types.Scope()
		for _, name := range scope.Names() {
			obj := scope.Lookup(name)
			named, ok := obj.Type().(*types.Named)
			if !ok {
				continue
			}
			struc, ok := named.Underlying().(*types.Struct)
			if !ok {
				continue
			}
			if !obj.Exported() {
				continue
			}
			if strings.HasSuffix(name, "List") || strings.HasSuffix(name, "Status") {
				continue
			}
			key := pkg.PkgPath + "." + name
			assertName := name + "Assertion"
			if nameCount[name] > 1 {
				// Disambiguate using a prefix derived from the package path
				assertName = pkgPrefix(pkg.PkgPath) + name + "Assertion"
			}
			knownStructs[key] = &structInfo{
				named:     named,
				struc:     struc,
				pkg:       pkg,
				assertNam: assertName,
			}
			orderedKeys = append(orderedKeys, key)
		}
	}
	sort.Strings(orderedKeys)

	// Helper: is this type a known struct from scanned packages?
	isKnownStruct := func(t types.Type) (string, bool) {
		t = deref(t)
		named, ok := t.(*types.Named)
		if !ok {
			return "", false
		}
		key := named.Obj().Pkg().Path() + "." + named.Obj().Name()
		info, ok := knownStructs[key]
		if !ok {
			return "", false
		}
		return info.assertNam, true
	}

	// Helper: is this type a slice whose element type is a known struct?
	isSliceOfKnownStruct := func(t types.Type) (string, bool) {
		sl, ok := t.(*types.Slice)
		if !ok {
			return "", false
		}
		return isKnownStruct(sl.Elem())
	}

	// Build the jennifer file
	f := NewFile(*pkgFlag)
	f.HeaderComment("Code generated by assertgen. DO NOT EDIT.")

	// Emit Opt[T] and Some[T]
	f.Comment("Opt wraps an optional value for assertion comparison.")
	f.Type().Id("Opt").Types(Id("T").Any()).Struct(
		Id("Val").Op("*").Id("T"),
	)
	f.Line()

	f.Comment("Some creates an Opt with a set value.")
	f.Func().Id("Some").Types(Id("T").Any()).Params(Id("v").Id("T")).Id("Opt").Types(Id("T")).Block(
		Return(Id("Opt").Types(Id("T")).Values(Dict{
			Id("Val"): Op("&").Id("v"),
		})),
	)
	f.Line()

	// Emit the Assertable marker interface
	f.Comment("Assertable is a marker interface implemented by all assertion structs.")
	f.Type().Id("Assertable").Interface(
		Id("isAssertable").Params(),
	)
	f.Line()

	// Emit assertion structs
	for _, key := range orderedKeys {
		info := knownStructs[key]
		struc := info.struc

		var fields []Code

		for i := 0; i < struc.NumFields(); i++ {
			field := struc.Field(i)
			if !field.Exported() {
				continue
			}

			fieldType := field.Type()
			// Dereference pointer for checking
			if assertName, ok := isKnownStruct(fieldType); ok {
				fields = append(fields, Id(field.Name()).Id(assertName))
			} else {
				fields = append(fields, Id(field.Name()).Id("Opt").Types(jenType(fieldType)))
			}
		}

		f.Comment(fmt.Sprintf("%s is the assertion struct for %s.", info.assertNam, info.named.Obj().Name()))
		f.Type().Id(info.assertNam).Struct(fields...)

		// Emit isAssertable method
		f.Func().Params(Id("_").Id(info.assertNam)).Id("isAssertable").Params().Block()
		f.Line()
	}

	// Generate a fresh file with the assertion structs and raw assertPartial
	f = NewFile(*pkgFlag)
	f.HeaderComment("Code generated by assertgen. DO NOT EDIT.")

	// Emit Opt[T] and Some[T]
	f.Comment("Opt wraps an optional value for assertion comparison.")
	f.Type().Id("Opt").Types(Id("T").Any()).Struct(
		Id("Val").Op("*").Id("T"),
	)
	f.Line()

	f.Comment("Some creates an Opt with a set value.")
	f.Func().Id("Some").Types(Id("T").Any()).Params(Id("v").Id("T")).Id("Opt").Types(Id("T")).Block(
		Return(Id("Opt").Types(Id("T")).Values(Dict{
			Id("Val"): Op("&").Id("v"),
		})),
	)
	f.Line()

	// Emit the Assertable marker interface
	f.Comment("Assertable is a marker interface implemented by all assertion structs.")
	f.Type().Id("Assertable").Interface(
		Id("isAssertable").Params(),
	)
	f.Line()

	// Emit assertion structs
	for _, key := range orderedKeys {
		info := knownStructs[key]
		struc := info.struc

		var fields []Code

		for i := 0; i < struc.NumFields(); i++ {
			field := struc.Field(i)
			if !field.Exported() {
				continue
			}

			fieldType := field.Type()
			if assertName, ok := isKnownStruct(fieldType); ok {
				fields = append(fields, Id(field.Name()).Id(assertName))
			} else if assertName, ok := isSliceOfKnownStruct(fieldType); ok {
				fields = append(fields, Id(field.Name()).Id("Opt").Types(Index().Id(assertName)))
			} else {
				fields = append(fields, Id(field.Name()).Id("Opt").Types(jenType(fieldType)))
			}
		}

		f.Comment(fmt.Sprintf("%s is the assertion struct for %s.", info.assertNam, info.named.Obj().Name()))
		f.Type().Id(info.assertNam).Struct(fields...)

		// Emit isAssertable method
		f.Func().Params(Id("_").Id(info.assertNam)).Id("isAssertable").Params().Block()
		f.Line()
	}

	// Render the jennifer portion
	jenBuf := &strings.Builder{}
	if err := f.Render(jenBuf); err != nil {
		log.Fatalf("rendering jennifer: %v", err)
	}

	// Now append the assertPartial function as raw Go code
	raw := jenBuf.String()

	// Add the assertPartial function
	raw += `
// Ptr returns a pointer to the given value.
// Use Ptr(int32(60)) instead of defining local int32Ptr closures.
func Ptr[T any](v T) *T { return &v }

// SomePtr is a convenience wrapper for Opt[*T] fields.
// Use SomePtr(true) instead of Some(boolPtr(true)).
func SomePtr[T any](v T) Opt[*T] { return Some(&v) }

var assertableType = reflect.TypeOf((*Assertable)(nil)).Elem()

// AssertPartial walks the assertion struct via reflection, comparing set fields against actual.
func AssertPartial[T Assertable](t *testing.T, actual any, assertion T, msgAndArgs ...any) {
	t.Helper()
	doAssertPartial(t, reflect.ValueOf(actual), reflect.ValueOf(assertion), msgAndArgs)
}

func doAssertPartial(t *testing.T, actualVal reflect.Value, assertionVal reflect.Value, msgAndArgs []any) {
	t.Helper()

	// Dereference pointers on actual
	for actualVal.Kind() == reflect.Ptr {
		if actualVal.IsNil() {
			require.Fail(t, "actual is nil but assertion has fields to check",
				append([]any{"got nil pointer when asserting against %v"}, msgAndArgs...)...)
			return
		}
		actualVal = actualVal.Elem()
	}

	assertionType := assertionVal.Type()

	for i := 0; i < assertionType.NumField(); i++ {
		fieldInfo := assertionType.Field(i)
		fieldVal := assertionVal.Field(i)

		// Get the corresponding field in actual
		actualField := actualVal.FieldByName(fieldInfo.Name)
		if !actualField.IsValid() {
			panic(fmt.Sprintf("assertPartial: actual struct %v does not have field %q", actualVal.Type(), fieldInfo.Name))
		}

		// Check if this field implements Assertable (nested assertion struct)
		if fieldVal.Type().Implements(assertableType) {
			// Skip if the field is zero value
			if fieldVal.IsZero() {
				continue
			}
			doAssertPartial(t, actualField, fieldVal, msgAndArgs)
			continue
		}

		// Must be Opt[T] — check if Val is nil (skip) or set (compare)
		valField := fieldVal.FieldByName("Val")
		if !valField.IsValid() {
			panic(fmt.Sprintf("assertPartial: field %q of type %v is neither Assertable nor Opt[T]", fieldInfo.Name, fieldVal.Type()))
		}
		if valField.IsNil() {
			continue
		}

		// val is set — compare
		expectedVal := valField.Elem()

		// Slice of Assertable: compare element-by-element with partial matching
		if expectedVal.Kind() == reflect.Slice && expectedVal.Type().Elem().Implements(assertableType) {
			require.Equal(t, expectedVal.Len(), actualField.Len(),
				append([]any{fmt.Sprintf("field %q: slice length mismatch", fieldInfo.Name)}, msgAndArgs...)...)
			for j := 0; j < expectedVal.Len(); j++ {
				elemMsg := append([]any{fmt.Sprintf("field %q[%d]", fieldInfo.Name, j)}, msgAndArgs...)
				doAssertPartial(t, actualField.Index(j), expectedVal.Index(j), elemMsg)
			}
			continue
		}

		// Map subset matching: verify all expected entries exist in actual.
		// This allows actual to have extra keys (e.g. Helm metadata annotations).
		// An empty expected map means "assert no entries" — treat nil actual as empty.
		if expectedVal.Kind() == reflect.Map {
			if expectedVal.Len() == 0 {
				// Expected empty map — actual must also be empty (or nil)
				if actualField.Kind() == reflect.Map && actualField.Len() > 0 {
					require.Fail(t, fmt.Sprintf("field %q: expected empty map but actual has %d entries", fieldInfo.Name, actualField.Len()),
						msgAndArgs...)
				}
				continue
			}
			for _, k := range expectedVal.MapKeys() {
				actualEntry := actualField.MapIndex(k)
				require.True(t, actualEntry.IsValid(),
					append([]any{fmt.Sprintf("field %q: expected key %v not found in actual map", fieldInfo.Name, k.Interface())}, msgAndArgs...)...)
				require.Equal(t, expectedVal.MapIndex(k).Interface(), actualEntry.Interface(),
					append([]any{fmt.Sprintf("field %q[%v] mismatch", fieldInfo.Name, k.Interface())}, msgAndArgs...)...)
			}
			continue
		}

		if !expectedVal.CanInterface() || !actualField.CanInterface() {
			require.True(t, reflect.DeepEqual(expectedVal.Interface(), actualField.Interface()),
				append([]any{fmt.Sprintf("field %q mismatch\nexpected: %v\nactual:   %v", fieldInfo.Name, expectedVal, actualField)}, msgAndArgs...)...)
			continue
		}
		require.Equal(t, expectedVal.Interface(), actualField.Interface(),
			append([]any{fmt.Sprintf("field %q mismatch", fieldInfo.Name)}, msgAndArgs...)...)
	}
}
`

	// Add missing imports for reflect, fmt, require
	// The jennifer file already handles imports for the struct types,
	// but we need to ensure reflect, fmt, and require are imported.
	// Let's inject them into the import block.
	raw = strings.Replace(raw,
		`import (`,
		`import (
	"fmt"
	"reflect"
	"testing"

	"github.com/stretchr/testify/require"`,
		1,
	)

	// Format the final output
	formatted, err := format.Source([]byte(raw))
	if err != nil {
		// Write unformatted for debugging
		_ = os.WriteFile(*outFlag+".raw", []byte(raw), 0644)
		log.Fatalf("gofmt: %v", err)
	}

	if err := os.WriteFile(*outFlag, formatted, 0644); err != nil {
		log.Fatalf("writing output: %v", err)
	}

	fmt.Printf("Generated %s (%d bytes)\n", *outFlag, len(formatted))
}

// pkgPrefix generates a disambiguation prefix from a package path.
// e.g. "k8s.io/apimachinery/pkg/runtime/schema" -> "Schema"
// e.g. "k8s.io/apimachinery/pkg/apis/meta/v1" -> "Meta"
func pkgPrefix(pkgPath string) string {
	parts := strings.Split(pkgPath, "/")
	// Walk backwards to find a meaningful name (skip version-like segments)
	for i := len(parts) - 1; i >= 0; i-- {
		seg := parts[i]
		if seg == "" {
			continue
		}
		// Skip version-like segments (v1, v2, v1beta1, etc.)
		if len(seg) >= 2 && seg[0] == 'v' && seg[1] >= '0' && seg[1] <= '9' {
			continue
		}
		// Capitalize the segment
		return strings.ToUpper(seg[:1]) + seg[1:]
	}
	return ""
}

// deref strips pointer indirection from a type.
func deref(t types.Type) types.Type {
	for {
		p, ok := t.(*types.Pointer)
		if !ok {
			return t
		}
		t = p.Elem()
	}
}

// jenType converts a go/types.Type to a jennifer Code statement.
func jenType(t types.Type) *Statement {
	switch tt := t.(type) {
	case *types.Named:
		obj := tt.Obj()
		pkg := obj.Pkg()
		if pkg == nil {
			// Built-in type
			return Id(obj.Name())
		}
		return Qual(pkg.Path(), obj.Name())

	case *types.Pointer:
		return Op("*").Add(jenType(tt.Elem()))

	case *types.Slice:
		return Index().Add(jenType(tt.Elem()))

	case *types.Map:
		return Map(jenType(tt.Key())).Add(jenType(tt.Elem()))

	case *types.Basic:
		return Id(tt.Name())

	case *types.Interface:
		if tt.NumMethods() == 0 {
			return Any()
		}
		return Id(tt.String())

	case *types.Array:
		return Index(Lit(int(tt.Len()))).Add(jenType(tt.Elem()))

	case *types.Struct:
		// Anonymous struct — just use any for simplicity
		return Any()

	default:
		return Id(t.String())
	}
}
